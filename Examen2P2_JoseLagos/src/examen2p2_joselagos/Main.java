    private void btn_GuardarC1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_GuardarC1MouseClicked
        AdminVehiculos av = new AdminVehiculos("./Vehiculos.rx");
        av.cargarArchivo();

        String marca = tf_MarcaCombustion1.getText();
        String modelo = tf_ModeloCombustion1.getText();
        int VIN = Integer.parseInt(tf_VINCombustion1.getText());
        String carroceria = "";

        if (rdb_FamiliarCombustion1.isSelected()) {
            carroceria += "Familiar";
        } else if (rdb_SUVCombustion1.isSelected()) {
            carroceria += "SUV";
        } else if (rdb_CompactoCombustion1.isSelected()) {
            carroceria += "Compacto";
        } else if (rdb_SubcomactCombustion1.isSelected()) {
            carroceria += "Subcompacto";
        } else if (rdb_DeportivoCombustion1.isSelected()) {
            carroceria += "Deportivo";
        }

        int cilindrada = (Integer) tf_Cilindrada1.getValue();
        int cilindros = 0;

        if ((Integer) js_Cilindros1.getValue() >= 3 && (Integer) js_Cilindros1.getValue() <= 8) {
            cilindros = (Integer) js_Cilindros1.getValue();
        } else if ((Integer) js_Cilindros1.getValue() < 3) {
            cilindros = 3;
        } else if ((Integer) js_Cilindros1.getValue() > 8) {
            cilindros = 8;
        }

        int consumo = (Integer) js_Consumo1.getValue();

        DefaultComboBoxModel model = (DefaultComboBoxModel) cb_Combustion.getModel();
        for (int i = 0; i < av.getLVehiculos().size(); i++) {
            if (av.getLVehiculos().get(i) instanceof Combustion) {
                if (av.getLVehiculos().get(i) == model.getSelectedItem()) {
                    ((Combustion) av.getLVehiculos().get(i)).setMarca(marca);
                    ((Combustion) av.getLVehiculos().get(i)).setModelo(modelo);
                    ((Combustion) av.getLVehiculos().get(i)).setVIN(VIN);
                    ((Combustion) av.getLVehiculos().get(i)).setCarroceria(carroceria);
                    ((Combustion) av.getLVehiculos().get(i)).setCilindrada(cilindrada);
                    ((Combustion) av.getLVehiculos().get(i)).setCilindros(cilindros);
                    ((Combustion) av.getLVehiculos().get(i)).setConsumo(consumo);

                    av.escribirArchivo();

                    JOptionPane.showMessageDialog(this, "El Vehiculo fue modificado");
                    jd_ModCombustion.dispose();
                }
            }

        }
    }//GEN-LAST:event_btn_GuardarC1MouseClicked

