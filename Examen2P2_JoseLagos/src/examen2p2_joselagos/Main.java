    private void jMenu1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu1MouseClicked
        AdminVehiculos av = new AdminVehiculos("./Vehiculos.rx");
        av.cargarArchivo();
        AdminBaterias ab = new AdminBaterias("./Baterias.rx");
        ab.cargarArchivoBateria();
        jl_ListaBaterias.setModel(new DefaultListModel());
        jl_ListaVehiculos.setModel(new DefaultListModel());

        DefaultListModel modelo = (DefaultListModel) jl_ListaVehiculos.getModel();
        DefaultListModel model = (DefaultListModel) jl_ListaBaterias.getModel();

        for (int i = 0; i < av.getLVehiculos().size(); i++) {

            if (av.getLVehiculos().get(i) instanceof Hibrido) {
                modelo.addElement((Hibrido) av.getLVehiculos().get(i));
                jl_ListaVehiculos.setModel(modelo);
            } else if (av.getLVehiculos().get(i) instanceof HibridoEnchufable) {
                modelo.addElement((HibridoEnchufable) av.getLVehiculos().get(i));
                jl_ListaVehiculos.setModel(modelo);

            } else if (av.getLVehiculos().get(i) instanceof Electrico) {
                modelo.addElement((Electrico) av.getLVehiculos().get(i));
                jl_ListaVehiculos.setModel(modelo);
            }
        }

        for (Baterias b : ab.getBaterias()) {
            model.addElement(b);
            jl_ListaBaterias.setModel(model);
        }

        jd_Ensamblaje.setModal(true);
        jd_Ensamblaje.pack();
        jd_Ensamblaje.setLocationRelativeTo(null);

        jd_Ensamblaje.setVisible(true);
    }//GEN-LAST:event_jMenu1MouseClicked

